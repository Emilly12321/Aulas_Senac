//Estrutura de repetição FOR
for(let i=0;i<=100;i++){
   console.log(`Loop ${i}`); 
}

for(let i=100; i>=0; i--){
    console.log(`Loop ${i}`);  
}

//Estrutura de repeticao WHILE
let i=0;
while(i<=10){
    console.log(`While ${i}`);
    i++;
}

//Estrutura de repeticao DO-WHILE
let j=0;
do{
    console.log(`DO-While ${j}`);
    j++;
}while(j<=10);